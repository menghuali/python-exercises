import math

print('=== hex, bin ===')
print(f'hex(12): {hex(12)}')
print(f'bin(12): {bin(12)}')

print('\n=== pow ===')
print(f'pow(2, 4):\t{pow(2, 4)}')
print(f'pow(2, 4, 3):\t{pow(2, 4, 3)}')

print('\n=== abs ===')
print(f'abs(-2): {abs(-2)}')

print('\n=== round ===')
print(f'round(3.4):\t{round(3.4)}')
print(f'round(3.5):\t{round(3.5)}')
print(f'round(3.5, 1):\t{round(3.5, 1)}')
print(f'round({math.pi}, 3):\t{round(math.pi, 3)}')